public class Kembangtelon extends Kucing {
    public Kembangtelon(String nama, double bobot) {
        super(nama, bobot);
    }

    public String getNama() {
        return nama;
    }

    public double getBobot() {
        return bobot;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public void setBobot(double bobot) {
        this.bobot = bobot;
    }


}